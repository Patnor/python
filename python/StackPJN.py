# Comment explaining a stack:
# A stack is a data structure that follows the Last-In-First-Out (LIFO) principle.
# It can be visualized as a stack of plates, where the last plate added is the first one to be removed.
# In a stack, elements can only be added or removed from the top, which is why it is also known as a "top-heavy" data structure.
# The operations commonly associated with a stack are push (add an element to the top), pop (remove and return the top element), peek (return the top element without removing it), is_empty (check if the stack is empty), and size (return the number of elements in the stack).


